#include <stdlib.h>

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (str != NULL)
	{
		while (str[i] != '\0')
		{
			if (str[i] < 32 || str[i] > 126)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
